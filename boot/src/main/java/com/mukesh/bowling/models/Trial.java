package com.mukesh.bowling.models;

public class Trial {

    Integer strike1;
    Integer strike1Score;
    Integer strike2;
    Integer strike2Score;
    Integer bonus;

    public Integer getStrike1() {
        return strike1;
    }

    public void setStrike1(Integer strike1) {
        this.strike1 = strike1;
    }

    public Integer getStrike1Score() {
        return strike1Score;
    }

    public void setStrike1Score(Integer strike1Score) {
        this.strike1Score = strike1Score;
    }

    public Integer getStrike2() {
        return strike2;
    }

    public void setStrike2(Integer strike2) {
        this.strike2 = strike2;
    }

    public Integer getStrike2Score() {
        return strike2Score;
    }

    public void setStrike2Score(Integer strike2Score) {
        this.strike2Score = strike2Score;
    }

    public Integer getBonus() {
        return bonus;
    }

    public void setBonus(Integer bonus) {
        this.bonus = bonus;
    }
}
